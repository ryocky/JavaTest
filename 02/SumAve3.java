//2つの変数xとyの合計と平均を表示(変数初期化)

public class SumAve3 {
    public static void main(String[] args){
        int x = 100;
        int y = 50;

        System.out.println("xの値は" + x);
        System.out.println("yの値は" + y);
        System.out.println("合計は" + (x + y));
        System.out.println("平均は" + (x + y) / 2);
    }
}
